/* Program to print all even no b/w 20 to 40
   and all odd no b/w 50 to 80*/
//Using for loop
#include<stdio.h>
void main()
{
int a,i;
printf("All Even no b/w 20 to 40 are\n");
for(i=22;i<=40;i=i+2)
{
printf(" %d ",i);
}
printf("\n\nAll Odd no b/w 50 to 80 are\n");
for(a=51;a<=80;a=a+2)
{
printf(" %d ",a);
}
}
